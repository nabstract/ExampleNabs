public class Poetry {
    private String title;
    private int sonnetNumber;
    private String sonnet;

    public void setTitle(String title) {
        this.title = title;
    }
    public String getTitle() {
        return title;
    }
    public void setSonnetNumber(int sonnetNumber) {
        this.sonnetNumber = sonnetNumber;
    }

    public int getSonnetNumber() {
        return sonnetNumber;
    }
    public void setSonnet(String sonnet) {
        this.sonnet = sonnet;
    }
    public String getSonnet() {
        return sonnet;
    }
}